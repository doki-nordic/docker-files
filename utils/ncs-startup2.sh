#!/bin/bash
set -e

find_file () {
	_path=. && [ -f $_path/$1 ] || \
	_path=.. && [ -f $_path/$1 ] || \
	_path=../.. && [ -f $_path/$1 ] || \
	_path=../../.. && [ -f $_path/$1 ] || \
	_path=../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=../../../../../../../../../../../../../../.. && [ -f $_path/$1 ] || \
	_path=
}

find_file zephyr/zephyr-env.sh
if [ ! -z "$_path" ]; then
	source $_path/zephyr/zephyr-env.sh
fi
